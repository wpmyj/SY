/*
*********************************************************************************************************
* Function Name : USBH_UserProcess
* Description	: USB用户回调函数
* Input			: None
* Output		: None
* Return		: None
*********************************************************************************************************
*/
void USBH_UserProcess(USBH_HandleTypeDef *phost, uint8_t id)
{
	switch(id)
	{ 
		case HOST_USER_SELECT_CONFIGURATION:
			break;

		case HOST_USER_DISCONNECTION:
		{			
//			FRESULT res = f_mount(NULL, (TCHAR const*)bsp_GetUSBRootPath(), 0);
//			if (res != FR_OK)
//			{
//				bsp_ErrorHandler();
//			}
			
			Appli_state = USB_HOST_DISCONNECT;
			ECHO(DEBUG_BSP_USB,"USB断开连接！");
			break;
		}
		case HOST_USER_CLASS_ACTIVE:
		{		
			FRESULT res = f_mount(&USBFatFs, (TCHAR const*)bsp_GetUSBRootPath(), 0);
			if (res != FR_OK)
			{
				bsp_ErrorHandler();
			}
			Appli_state = USB_HOST_READY;
			ECHO(DEBUG_BSP_USB,"USB已就绪！");
			break;
		}
		case HOST_USER_CONNECTION:
		{
			Appli_state = USB_HOST_CONNECT;
			ECHO(DEBUG_BSP_USB,"USB建立连接！");
			break;
		}
		default:
			break; 
	}
}

